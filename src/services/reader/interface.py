from abc import ABC, abstractmethod


class IReader(ABC):
    """
    Интерфейс для синхронного компонента чтения данных.

    Используется для реализации блокирующих процессов чтения, которые
    выполняются в отдельном потоке или в простых синхронных скриптах.
    """

    @abstractmethod
    def start_reading(self) -> None:
        """
        Запускает цикл чтения данных.

        Предполагается, что метод инициализирует соединение с источником
        и начинает обработку входящих сообщений.
        """
        ...


class IAsyncReader(ABC):
    """
    Интерфейс для асинхронного компонента чтения данных.

    Предназначен для использования в асинхронных приложениях (на базе asyncio),
    где чтение данных не должно блокировать основной поток выполнения.
    """

    @abstractmethod
    async def start_reading(self) -> None:
        """
        Асинхронно запускает цикл чтения данных.

        Метод инициализирует асинхронное соединение (например, aiokafka)
        и запускает бесконечный цикл обработки событий.
        """
        ...
